def get_questions_for_week(week_number: int):
    # Если номер недели больше 8, начинаем цикл заново (1-8)
    adjusted_week = ((week_number - 1) % 8) + 1
    return {
        "core": CORE_QUESTIONS,
        "variative": VARIATIVE_QUESTIONS[adjusted_week],
        "week_number": week_number
    }
